const { Document, Paragraph, TextRun, Table, TableCell, TableRow, AlignmentType, HeadingLevel } = require('docx');
const ExcelJS = require('exceljs');
const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');

// Generate DOCX report
async function generateDOCX(scanData) {
    const { targetUrl, results, startTime, scanId } = scanData;

    // Count vulnerabilities by severity
    const severityCounts = {
        CRITICAL: 0,
        HIGH: 0,
        MEDIUM: 0,
        LOW: 0,
        INFO: 0
    };

    results.forEach(vuln => {
        severityCounts[vuln.severity] = (severityCounts[vuln.severity] || 0) + 1;
    });

    const doc = new Document({
        sections: [{
            properties: {},
            children: [
                // Title
                new Paragraph({
                    text: 'Security Vulnerability Report',
                    heading: HeadingLevel.HEADING_1,
                    alignment: AlignmentType.CENTER
                }),

                // Report info
                new Paragraph({
                    text: `Target: ${targetUrl}`,
                    spacing: { before: 400 }
                }),
                new Paragraph({
                    text: `Scan Date: ${new Date(startTime).toLocaleString()}`,
                }),
                new Paragraph({
                    text: `Scan ID: ${scanId}`,
                    spacing: { after: 400 }
                }),

                // Executive Summary
                new Paragraph({
                    text: 'Executive Summary',
                    heading: HeadingLevel.HEADING_2,
                    spacing: { before: 400 }
                }),
                new Paragraph({
                    text: `Total Vulnerabilities Found: ${results.length}`,
                    spacing: { before: 200 }
                }),
                new Paragraph({
                    text: `Critical: ${severityCounts.CRITICAL} | High: ${severityCounts.HIGH} | Medium: ${severityCounts.MEDIUM} | Low: ${severityCounts.LOW} | Info: ${severityCounts.INFO}`,
                    spacing: { after: 400 }
                }),

                // Vulnerabilities
                new Paragraph({
                    text: 'Detailed Findings',
                    heading: HeadingLevel.HEADING_2,
                    spacing: { before: 400, after: 200 }
                }),

                // Add each vulnerability
                ...results.flatMap((vuln, index) => [
                    new Paragraph({
                        text: `${index + 1}. ${vuln.title}`,
                        heading: HeadingLevel.HEADING_3,
                        spacing: { before: 300 }
                    }),
                    new Paragraph({
                        children: [
                            new TextRun({ text: 'Severity: ', bold: true }),
                            new TextRun({ text: vuln.severity })
                        ]
                    }),
                    new Paragraph({
                        children: [
                            new TextRun({ text: 'Description: ', bold: true }),
                            new TextRun({ text: vuln.description })
                        ],
                        spacing: { before: 100 }
                    }),
                    ...(vuln.details ? [new Paragraph({
                        children: [
                            new TextRun({ text: 'Details: ', bold: true }),
                            new TextRun({ text: vuln.details })
                        ],
                        spacing: { before: 100 }
                    })] : []),
                    ...(vuln.remediation ? [new Paragraph({
                        children: [
                            new TextRun({ text: 'Remediation: ', bold: true }),
                            new TextRun({ text: vuln.remediation })
                        ],
                        spacing: { before: 100, after: 200 }
                    })] : [])
                ]),

                // Footer
                new Paragraph({
                    text: '---',
                    spacing: { before: 400 }
                }),
                new Paragraph({
                    text: 'Generated by AI Cybersecurity Platform',
                    alignment: AlignmentType.CENTER
                }),
                new Paragraph({
                    text: 'https://github.com/your-platform',
                    alignment: AlignmentType.CENTER
                })
            ]
        }]
    });

    return doc;
}

// Generate Excel report
async function generateExcel(scanData) {
    const { targetUrl, results, startTime, scanId } = scanData;

    const workbook = new ExcelJS.Workbook();
    const worksheet = workbook.addWorksheet('Vulnerability Report');

    // Set column widths
    worksheet.columns = [
        { header: '#', key: 'index', width: 5 },
        { header: 'Severity', key: 'severity', width: 12 },
        { header: 'Title', key: 'title', width: 40 },
        { header: 'Description', key: 'description', width: 50 },
        { header: 'Details', key: 'details', width: 50 },
        { header: 'Remediation', key: 'remediation', width: 50 }
    ];

    // Add report header
    worksheet.addRow([]);
    const titleRow = worksheet.addRow(['Security Vulnerability Report']);
    titleRow.font = { size: 16, bold: true };
    titleRow.alignment = { horizontal: 'center' };
    worksheet.mergeCells('A2:F2');

    worksheet.addRow([]);
    worksheet.addRow(['Target:', targetUrl]);
    worksheet.addRow(['Scan Date:', new Date(startTime).toLocaleString()]);
    worksheet.addRow(['Scan ID:', scanId]);
    worksheet.addRow(['Total Vulnerabilities:', results.length]);
    worksheet.addRow([]);

    // Style header row
    const headerRow = worksheet.getRow(9);
    headerRow.font = { bold: true };
    headerRow.fill = {
        type: 'pattern',
        pattern: 'solid',
        fgColor: { argb: 'FF00FF88' }
    };

    // Add vulnerabilities
    results.forEach((vuln, index) => {
        const row = worksheet.addRow({
            index: index + 1,
            severity: vuln.severity,
            title: vuln.title,
            description: vuln.description,
            details: vuln.details || 'N/A',
            remediation: vuln.remediation || 'N/A'
        });

        // Color code by severity
        const severityColors = {
            'CRITICAL': 'FFFF4757',
            'HIGH': 'FFFF6348',
            'MEDIUM': 'FFFFA502',
            'LOW': 'FF26DE81',
            'INFO': 'FF00CCFF'
        };

        const color = severityColors[vuln.severity] || 'FFFFFFFF';
        row.getCell('severity').fill = {
            type: 'pattern',
            pattern: 'solid',
            fgColor: { argb: color }
        };
    });

    return workbook;
}

// Generate PDF report
async function generatePDF(scanData, outputPath) {
    return new Promise((resolve, reject) => {
        const { targetUrl, results, startTime, scanId } = scanData;

        const doc = new PDFDocument({ margin: 50 });
        const stream = fs.createWriteStream(outputPath);

        doc.pipe(stream);

        // Title
        doc.fontSize(24)
            .text('Security Vulnerability Report', { align: 'center' })
            .moveDown();

        // Report info
        doc.fontSize(12)
            .text(`Target: ${targetUrl}`)
            .text(`Scan Date: ${new Date(startTime).toLocaleString()}`)
            .text(`Scan ID: ${scanId}`)
            .moveDown();

        // Summary
        doc.fontSize(16)
            .text('Executive Summary')
            .fontSize(12)
            .text(`Total Vulnerabilities Found: ${results.length}`)
            .moveDown();

        // Vulnerabilities
        doc.fontSize(16)
            .text('Detailed Findings')
            .moveDown();

        results.forEach((vuln, index) => {
            doc.fontSize(14)
                .text(`${index + 1}. ${vuln.title}`)
                .fontSize(12)
                .text(`Severity: ${vuln.severity}`, { indent: 20 })
                .text(`Description: ${vuln.description}`, { indent: 20 });

            if (vuln.details) {
                doc.text(`Details: ${vuln.details}`, { indent: 20 });
            }

            if (vuln.remediation) {
                doc.text(`Remediation: ${vuln.remediation}`, { indent: 20 });
            }

            doc.moveDown();
        });

        // Footer
        doc.fontSize(10)
            .text('---', { align: 'center' })
            .text('Generated by AI Cybersecurity Platform', { align: 'center' });

        doc.end();

        stream.on('finish', () => resolve(outputPath));
        stream.on('error', reject);
    });
}

module.exports = {
    generateDOCX,
    generateExcel,
    generatePDF
};
